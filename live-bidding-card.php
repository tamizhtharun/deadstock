<script>
// Set the timer deadline (50 days, 24 hours, 60 minutes, 60 seconds)
const deadlines = Array.from(document.querySelectorAll('.timer')).map(() => {
  const now = new Date().getTime();
  return now + (50 * 24 * 60 * 60 * 1000) + (24 * 60 * 60 * 1000) + (60 * 60 * 1000) + (60 * 1000) + 1000; // Adjust the duration here
});

function updateTimers() {
  const now = new Date().getTime();
  deadlines.forEach((deadline, index) => {
    const distance = deadline - now;

    if (distance >= 0) {
      const days = Math.floor(distance / (1000 * 60 * 60 * 24));
      const hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
      const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
      const seconds = Math.floor((distance % (1000 * 60)) / 1000);

      document.querySelectorAll(".timer")[index].innerText = `${days}d ${hours}h ${minutes}m ${seconds}s`;
    } else {
      document.querySelectorAll(".timer")[index].innerText = "Time's up!";
    }
  });
}

// Start updating the timers
const timerInterval = setInterval(updateTimers, 1000);
updateTimers();

</script>
